    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
        int check = validateInputs();
        if(check==0){
            JOptionPane.showMessageDialog(null, "Kindly plz fill all the fields first !!", "Incomplete Information", JOptionPane.WARNING_MESSAGE);
            return;
        }
        if(check==-1){
            JOptionPane.showMessageDialog(null, "Length of the password must be greater than or equal to 4 !!", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        if(check==-2){
            JOptionPane.showMessageDialog(null, "Both the password doesn't match , kindly fill the same password in both the field !!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if(check==-3){
            JOptionPane.showMessageDialog(null, "You have entered the Previous Password , kindly please mention NEW PASSWORD!!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        try{
            if(UserDao.updatePassword(password, userid, usertype)){
                JOptionPane.showMessageDialog(null,"Congratulations !! Your Password have been Reset Successfully ..","Success",JOptionPane.INFORMATION_MESSAGE);
            }
        }
        catch(SQLException ex){
            JOptionPane.showMessageDialog(null,"Error in DB , In ResetPasswordFrame","Error",JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnRegisterActionPerformed


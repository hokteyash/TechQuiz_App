    private void loadOldPassword() {
        try{
            oldPassword = UserDao.getPassword(userid, usertype);
        }
        catch(SQLException ex){
            JOptionPane.showMessageDialog(null,"Error in DB , In ResetPasswordFrame","Error",JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }

    private int validateInputs() {
        char []pwd = passwordField.getPassword();
        char []rePwd = rePasswordField.getPassword();
        if(pwd.length==0 || rePwd.length==0) return 0;
        if(pwd.length<4) return -1;
        password = new String(pwd);
        rePassword = new String(rePwd);
        if(password.equals(rePassword)==false) return -2;
        if(password.equals(oldPassword)) return -3;
        return 1;
    }

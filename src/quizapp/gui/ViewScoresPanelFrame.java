    private void btnBackActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBackActionPerformed
       int sure = JOptionPane.showConfirmDialog(null,"Are you sure you want to go back ?","Sure?",JOptionPane.YES_NO_OPTION,JOptionPane.INFORMATION_MESSAGE);
       if(sure==JOptionPane.YES_OPTION){
           JFrame fr = null;
           if(UserProfile.getUsertype().equals("Admin")){
               fr = new AdministrationOptionsFrame();
           }
           else{
               fr = new StudentOptionsFrame();
           }
           fr.setVisible(true);
           this.dispose();
       }
    }//GEN-LAST:event_btnBackActionPerformed


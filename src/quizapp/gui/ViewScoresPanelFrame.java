    private void btnDoTaskActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDoTaskActionPerformed
        if(validateInputs()==false){
            JOptionPane.showMessageDialog(null,"Please make a selection first !!","Incomplete Information!!",JOptionPane.WARNING_MESSAGE);
            return;
        }
        if(jrbViewSingleStudentScore.isSelected()){
            ViewSingleStudentScoreFrame fr = new ViewSingleStudentScoreFrame();
            fr.setVisible(true);
        }
        else{
            ViewAllScoresFrame fr = new ViewAllScoresFrame();
            fr.setVisible(true);
        }
        this.dispose();
    }//GEN-LAST:event_btnDoTaskActionPerformed


    private void btnQuitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnQuitActionPerformed
        int ans = JOptionPane.showConfirmDialog(null, "Are you sure you want to quit the application ?","Quitting",JOptionPane.YES_NO_OPTION,JOptionPane.QUESTION_MESSAGE);
        if(ans==JOptionPane.YES_OPTION){
            DBConnection.closeConnection();
            JOptionPane.showMessageDialog(null, "Thankyou for using the App !!","Quit",JOptionPane.INFORMATION_MESSAGE);
            System.exit(0);
        }
        return;
    }//GEN-LAST:event_btnQuitActionPerformed

    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        if(validateInputs()==false){
            JOptionPane.showMessageDialog(null, "Please fill all the data in the fields","Incomplete data",JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        String userType = validateUserType();
        if(userType==null){
            JOptionPane.showMessageDialog(null, "Please select atleast one field from Admin or Student","Incomplete data",JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        try{
            User user = new User(username,password,userType);
            if(UserDao.validateUser(user)==false){
                JOptionPane.showMessageDialog(null, "Invalid Credentials","Login Denied",JOptionPane.ERROR_MESSAGE);
                return;
            }
            JOptionPane.showMessageDialog(null, "Login Successfully !!","Success",JOptionPane.INFORMATION_MESSAGE);
            UserProfile.setUsername(username);
            UserProfile.setUsertype(userType);
            if(jrbAdmin.isSelected()){
                AdministrationOptionsFrame ad = new AdministrationOptionsFrame();
                ad.setVisible(true);
            }
            else{
                StudentOptionsFrame fr = new StudentOptionsFrame();
                fr.setVisible(true);
            }
            this.dispose();
        }
        catch(SQLException ex){
            JOptionPane.showMessageDialog(null, "Can't be Login due to issue in DB !!","Error",JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnLoginActionPerformed


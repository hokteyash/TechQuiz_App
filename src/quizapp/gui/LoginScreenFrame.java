    private boolean validateInputs() {
        username = jtxtUsername.getText();
        password = String.valueOf(jPasswordField.getPassword());
        if(username.isEmpty()||password.isEmpty()) return false;
        return true;
    }

    private String validateUserType() {
       if(jrbAdmin.isSelected()) return jrbAdmin.getText();
       else if(jrbStudent.isSelected()) return jrbStudent.getText();
       else return null;
    }
   

    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
        int check = validateInputs();
        if(check==0){
            JOptionPane.showMessageDialog(null, "Kindly plz fill all the fields first !!", "Incomplete Information", JOptionPane.WARNING_MESSAGE);
            return;
        }
        if(check==-1){
            JOptionPane.showMessageDialog(null, "Length of the userid or password must be greater than or equal to 4 !!", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        if(check==-2){
            JOptionPane.showMessageDialog(null, "Both the password doesn't match , kindly fill the same password in both the field !!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        User user = new User(userid,password,"Student");
        try{
            if(UserDao.addUser(user)==false){
                JOptionPane.showMessageDialog(null, " User already present,kindly enter the unique userid/username !!", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            JOptionPane.showMessageDialog(null, "Registeration Successfully done !!", "Success", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        catch(SQLException ex){
            JOptionPane.showMessageDialog(null, "Error in Registering the student,kindly plz try again !!"+ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
            return;
        }
    }//GEN-LAST:event_btnRegisterActionPerformed


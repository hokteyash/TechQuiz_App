    private int validateInputs() {
        if(jtxtUserId.getText().trim().isEmpty() || passwordField.getPassword().length==0 || rePasswordField.getPassword().length==0){
            return 0;
        }
        userid = jtxtUserId.getText().trim();
        char[]pwd = passwordField.getPassword();
        char[]rePwd = rePasswordField.getPassword();
        if(userid.length()<4 || pwd.length<4){
            return -1;
        }
        password = new String(pwd).trim();
        rePassword = new String(rePwd).trim();
        if(password.equals(rePassword)==false){
            return -2;
        }
        return 1;
    }

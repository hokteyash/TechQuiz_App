    private void btnNextActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNextActionPerformed
       String chosenAnswer = getUserAnswer();
       if(chosenAnswer!=null){
           Question quest = ques.getQuestionByQno(qno);
           String correctAnswer = quest.getCorrect_answer();
           Answer ans = new Answer(exam.getExamid(),exam.getLanguage(),qno,chosenAnswer,correctAnswer);
           Answer attempted = astore.getAnswerByQno(qno);
           if(attempted==null){
               astore.addAnswer(ans);
           }
           else{
               String prevAnswer = attempted.getChosenAnswer();
               if(prevAnswer.equals(chosenAnswer)==false){
                   int apos = astore.removeAnswer(attempted);
                   astore.setAnswerAt(apos, ans);
               }
           }
           pos++;
        if(pos>=ques.getSize()){
            pos=0;
        }
        qno++;
        if(qno>ques.getSize()){
            JOptionPane.showMessageDialog(null,"You are done with all the questions , if you have still remains with some questions which are unattempted , kindly attempt that or to submit the test , CLICK on DONE button !! ");
            qno=1;
        }
        lblQno.setText("Que "+qno);
        showQuestions();
       }
    }//GEN-LAST:event_btnNextActionPerformed


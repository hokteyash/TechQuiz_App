    private void btnDoneActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDoneActionPerformed
        if(qno!=ques.getSize()){
            int sure = JOptionPane.showConfirmDialog(null, "You are remains with some more questions in your test !! , still you wants to submit the test \nClick on Yes if you want to submit the test\nOtherwise click on Next button to solve the next question !!","Are you sure?",JOptionPane.YES_NO_OPTION,JOptionPane.QUESTION_MESSAGE);
            if(sure==JOptionPane.NO_OPTION) return;
        }
        String chosenAnswer = getUserAnswer();
        if(chosenAnswer!=null){
           Question quest = ques.getQuestionByQno(qno);
           String correctAnswer = quest.getCorrect_answer();
           Answer ans = new Answer(exam.getExamid(),exam.getLanguage(),qno,chosenAnswer,correctAnswer);
           Answer attempted = astore.getAnswerByQno(qno);
           if(attempted==null){
               astore.addAnswer(ans);
           }
           else{
               String prevAnswer = attempted.getChosenAnswer();
               if(prevAnswer.equals(chosenAnswer)==false){
                   int apos = astore.removeAnswer(attempted);
                   astore.setAnswerAt(apos, ans);
               }
           }
       }
       
       int right=0,wrong=0;
       for(Question quest:ques.getAllQuestions()){
           int qno = quest.getQno();
           Answer ans = astore.getAnswerByQno(qno);
           if(ans==null) continue;
           String attemptedAnswer = ans.getChosenAnswer();
           String correctAnswer = ans.getCorrectAnswer();
           if(attemptedAnswer.equals(correctAnswer)) ++right;
           else ++wrong;
       }
       
       StringBuilder sb = new StringBuilder();
       sb.append("Right answers : "+right);
       sb.append("\nWrong answers : "+wrong);
       sb.append("\nUnattempted answers : "+(ques.getSize()-(right+wrong)));
       JOptionPane.showMessageDialog(null,sb.toString(),"Result !",JOptionPane.INFORMATION_MESSAGE);
       double per = (double)right/ques.getSize()*100.0;
       try{
           Performance perf = new Performance(UserProfile.getUsername(),exam.getExamid(),right,wrong,(ques.getSize()-(right+wrong)),per,exam.getLanguage());
           PerformanceDao.addPerformance(perf);
           JOptionPane.showMessageDialog(null,"Result Saved!!\nThankyou for the test","Done !!",JOptionPane.INFORMATION_MESSAGE);
           StudentOptionsFrame fr = new StudentOptionsFrame();
           fr.setVisible(true);
           this.dispose();
       }
       catch(SQLException ex){
          JOptionPane.showMessageDialog(null,"DB Error , In Take Test Frame","Error !!",JOptionPane.ERROR_MESSAGE);
          ex.printStackTrace();
       }
    }//GEN-LAST:event_btnDoneActionPerformed

